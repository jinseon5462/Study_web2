document.write('hello~javascript~~');
// const(상수) : 한번 값을 할당하면 변경할 수 없다.
// var, let : 값을 재할당할 수 있다.
// 요즘은 let, const를 사용한다.
// 자바스크립트에는 자료형의 개념이 없다.

let val = 1;
document.write(val + '<br>');

val = 12.345;
document.write(val + '<br>');

val = '안녕하세요';
document.write(val + '<br>');

val = true;
document.write(val + '<br>');

let val2;
document.write(val2 + '<br>');
// undefined 한번도 정의되지 않을경우